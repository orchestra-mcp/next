/**
 * Generate a TypeScript paths map from all Boxicons SVGs.
 *
 * Usage: node scripts/generate-boxicon-components.mjs
 *
 * Reads every SVG from boxicons/svg/{regular,solid,logos}/ and
 * extracts the inner SVG content (path elements). Outputs a
 * static map at src/boxicons/paths.ts keyed by icon name.
 *
 * Categories:
 *   - regular: bx-*   (~814 icons)
 *   - solid:   bxs-*  (~665 icons)
 *   - logos:   bxl-*  (~155 icons)
 */

import { readFileSync, writeFileSync, readdirSync, mkdirSync } from 'node:fs';
import { join, dirname, basename } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const BOXICONS_SVG = join(ROOT, 'node_modules/boxicons/svg');
const OUT_DIR = join(ROOT, 'src/boxicons');

const categories = ['regular', 'solid', 'logos'];

function extractInnerSvg(svgContent) {
  return svgContent
    .replace(/<svg[^>]*>/, '')
    .replace(/<\/svg>\s*$/, '')
    .trim();
}

const entries = [];

for (const category of categories) {
  const dir = join(BOXICONS_SVG, category);
  let files;
  try {
    files = readdirSync(dir).filter((f) => f.endsWith('.svg'));
  } catch {
    console.warn(`Skipping missing directory: ${dir}`);
    continue;
  }

  for (const file of files) {
    const name = basename(file, '.svg');
    const svg = readFileSync(join(dir, file), 'utf-8');
    const inner = extractInnerSvg(svg);
    entries.push({ name, inner });
  }
}

entries.sort((a, b) => a.name.localeCompare(b.name));
mkdirSync(OUT_DIR, { recursive: true });

const output = `/**
 * Auto-generated Boxicons SVG path data.
 * Generated by: node scripts/generate-boxicon-components.mjs
 *
 * DO NOT EDIT — regenerate with the script above.
 * Icons from Boxicons (https://boxicons.com), MIT License.
 *
 * Total icons: ${entries.length}
 */

export const boxiconPaths: Record<string, string> = {
${entries.map(({ name, inner }) => {
  const escaped = inner.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
  return `  '${name}': '${escaped}',`;
}).join('\n')}
};

export const boxiconNames = Object.keys(boxiconPaths) as string[];
`;

const outPath = join(OUT_DIR, 'paths.ts');
writeFileSync(outPath, output, 'utf-8');

const regular = entries.filter((e) => e.name.startsWith('bx-')).length;
const solid = entries.filter((e) => e.name.startsWith('bxs-')).length;
const logos = entries.filter((e) => e.name.startsWith('bxl-')).length;

console.log(`Generated: ${outPath}`);
console.log(`Regular (bx-):  ${regular}`);
console.log(`Solid (bxs-):   ${solid}`);
console.log(`Logos (bxl-):   ${logos}`);
console.log(`Total icons:    ${entries.length}`);
